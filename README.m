# Basic Quiz Game

This is a simple quiz game built with Python. It asks users a series of questions and evaluates their performance based on their answers.

## Features

- Multiple-choice questions
- Scoring system
- User input validation
- Feedback on answers
- Final score display

## How to Run

1. Clone the repository:

   git clone https://github.com/SunkavalliSahithi/Basic-quiz-game.git
   cd basic-quiz-game





